<h1>Loops</h1>

<?php
//while loop

$min = 0;
while($min <10){
    print $min . "<br>";
    $min++;
}
?>
<h4>do-while loop</h4>
<?php
//do-while loop
$x = 14;
do{
    print $x . "<br>";
    $x++;
}while($x < 7);
?>
<h4>for loop</h4>
<?php
    //for loop
    for($r=3; $r<12; $r++){
        print $r ."<br>"; 
    }
?>
