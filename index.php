    <h1 style="background-color: bisque;">Brief History About Restaurants</h1>
    <p>Dining facilities have a long history, dating back to the eighteenth century.
     The first restaurant believed to have opened in France in 1762, specialized in soups and other simple dishes (Walker, 2021). Although the restaurant wasn’t the first to offer a menu with a selection of options, 
     it was the first to enable clients to place food orders.
     Before this, people could purchase food at roadside stores, but the choice was typically quite limited.
     America experienced unprecedented growth during the nineteenth century, particularly in the transportation sector. Longer distance commute was made possible by railroads, which raised the demand for dining establishments.
      In the beginning,American eateries tried replicating French ones using American-made components. When the 20th century arrived, the hospitality industry began to capture American lifestyles.
       Fast food establishments like McDonald’s grew in popularity in the middle of the twentieth century as people had less and less time for eating.</p>
    
